//espacio de nombres.
using namespace UPC;

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#1: constructor 1.
BUFF_STR_CAN::BUFF_STR_CAN()
{
	//---------------------------------------------------------------------------------
	//inicialización de parámetros estructurales.	
	this->STR_ID			= -1;															//id de la estructura.	
	this->UPCI_ID			= -1;															//id de upci de estructura.	
	this->UPCI_TYPE			= -1;															//tipo de upci.

	//---------------------------------------------------------------------------------
	//inicialización de nivel de congruencia de la estructura.
	this->CONGRUENCE_LEVEL	= -1;														
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#2: constructor 2.
BUFF_STR_CAN::BUFF_STR_CAN(in _str_id, in _upci_type, in _upci_id, dou _congruence_level)
{
	//---------------------------------------------------------------------------------
	//seteo de parámetros estructurales.	
	this->STR_ID			= _str_id;														//id estructura.	
	this->UPCI_ID			= _upci_id;														//id de upci.	
	this->UPCI_TYPE			= _upci_type;													//tipo de upci.

	//---------------------------------------------------------------------------------
	//seteo de nivel de congruencia de la estructura.
	this->CONGRUENCE_LEVEL	= _congruence_level;												
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#3: destructor.
BUFF_STR_CAN::~BUFF_STR_CAN()
{
	//---------------------------------------------------------------------------------
	//limpieza de buffer.
	this->Clean();
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#1: método para setear la estructura.
vo	BUFF_STR_CAN::Set(in _str_id, in _upci_type, in _upci_id, dou _congruence_level)
{
	//---------------------------------------------------------------------------------
	//seteo de parámetros estructurales.
	this->STR_ID			= _str_id;														//id estructura.	
	this->UPCI_ID			= _upci_id;														//id de upci.	
	this->UPCI_TYPE			= _upci_type;													//tipo de upci.

	//---------------------------------------------------------------------------------
	//seteo de nivel de congruencia de la estructura.
	this->CONGRUENCE_LEVEL	= _congruence_level;	
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#1: método para Clean la estructura.
vo BUFF_STR_CAN::Clean()
{
	//---------------------------------------------------------------------------------
	//limpieza parámetros estructurales.
	this->STR_ID			= -1;															//id de la estructura.	
	this->UPCI_ID			= -1;															//id de upci de estructura.	
	this->UPCI_TYPE			= -1;															//tipo de upci.

	//---------------------------------------------------------------------------------
	//limpieza de nivel de congruencia de la estructura.
	this->CONGRUENCE_LEVEL	= -1;	
}