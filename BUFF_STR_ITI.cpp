#include "BUFF_STR_ITI.h"

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//espacio de nombres.
using namespace UPC;

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#1: constructor 1.
BUFF_STR_ITI::BUFF_STR_ITI()
{
	//---------------------------------------------------------------------------------
	//inicialización de parámetros
	this->STR_ID	= -1;																	//id estructura.
	this->UPCI_TYPE	= -1;																	//tipo de upci.
	this->UPCI_ID	= -1;																	//id upci.
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#2: constructor 2.
BUFF_STR_ITI::BUFF_STR_ITI(in _str_id, in _upci_type, in _upci_id)
{
	//---------------------------------------------------------------------------------
	//seteo de parámetros estructurales.
	this->STR_ID	= _str_id;																//id estructura.
	this->UPCI_TYPE	= _upci_type;															//tipo de upci.
	this->UPCI_ID	= _upci_id;																//id upci.	
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#3: destructor.
BUFF_STR_ITI::~BUFF_STR_ITI()
{
	//---------------------------------------------------------------------------------
	//limpieza de buffer.
	this->Clean();
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#1: método para setear la estructura.
vo	BUFF_STR_ITI::Set(in _str_id, in _upci_type, in _upci_id)
{
	//---------------------------------------------------------------------------------
	//seteo de parámetros estructurales.	
	this->STR_ID	= _str_id;																//id estructura.
	this->UPCI_TYPE	= _upci_type;															//tipo de upci.
	this->UPCI_ID	= _upci_id;																//id upci.
}	

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#1: método para Clean la estructura.
vo BUFF_STR_ITI::Clean()
{
	//---------------------------------------------------------------------------------
	//limpieza parámetros estructurales.
	this->STR_ID	= -1;																	//id estructura.	
	this->UPCI_TYPE	= -1;																	//tipo de upci.
	this->UPCI_ID	= -1;																	//id upci.
}
