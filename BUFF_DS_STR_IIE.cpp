using namespace UPC;

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#1: constructor 1.
BUFF_DS_STR_IIE::BUFF_DS_STR_IIE()
{
	//---------------------------------------------------------------------------------
	//inicialización de parámetros estructurales.
	this->UPCI_ID						= -1;													//id de upci.	
	this->STR_ID						= -1;													//id estructura.

	//---------------------------------------------------------------------------------
	//inicialización de diferencia de evaluación d entradas.
	this->INPUT_EVALUATION_DIFFERENCE	= -1;													
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#2: constructor 2.
BUFF_DS_STR_IIE::BUFF_DS_STR_IIE(in _upci_id, in _str_id, dou _input_evaluation_difference)
{
	//---------------------------------------------------------------------------------
	//seteo de parámetros estructurales.
	this->UPCI_ID						= _upci_id;												//id de upci.	
	this->STR_ID						= _str_id;												//id estructura.

	//---------------------------------------------------------------------------------
	//seteo de diferencia de evaluación d entradas..
	this->INPUT_EVALUATION_DIFFERENCE	= _input_evaluation_difference;
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#3: destructor.
BUFF_DS_STR_IIE::~BUFF_DS_STR_IIE()
{
	//---------------------------------------------------------------------------------
	//limpieza de buffer.
	this->Clean();
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#1: método para setear el id de la upci.
vo BUFF_DS_STR_IIE::SetUPCI_ID(in _upci_id)
{
	//---------------------------------------------------------------------------------
	//seteo del id de la upci.
	this->UPCI_ID	= _upci_id;													
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#2: método para setear el id de la estructura.
vo BUFF_DS_STR_IIE::SetSTR_ID(in _str_id)
{
	//---------------------------------------------------------------------------------
	//seteo del id de la estructura.
	this->STR_ID	= _str_id;												
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#3: método para setear la diferencia de evaluación d entradas.
vo BUFF_DS_STR_IIE::SetINPUT_EVALUATION_DIFFERENCE(dou _input_evaluation_difference)
{
	//---------------------------------------------------------------------------------
	//seteo de diferencia de evaluación d entradas..
	this->INPUT_EVALUATION_DIFFERENCE = _input_evaluation_difference;
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#4: método para setear la estructura.
vo BUFF_DS_STR_IIE::Set(in _upci_id, in _str_id, dou _input_evaluation_difference)
{
	//---------------------------------------------------------------------------------
	//seteo de parámetros estructurales.
	this->UPCI_ID						= _upci_id;												//id de upci.	
	this->STR_ID						= _str_id;												//id estructura.

	//---------------------------------------------------------------------------------
	//seteo de diferencia de evaluación d entradas..
	this->INPUT_EVALUATION_DIFFERENCE	= _input_evaluation_difference;
}	

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//#1: método para Clean la estructura.
vo BUFF_DS_STR_IIE::Clean()
{
	//---------------------------------------------------------------------------------
	//limpieza de diferencia de evaluación d entradas..
	this->UPCI_ID						= -1;													//id de upci.	
	this->STR_ID						= -1;													//id estructura.

	//---------------------------------------------------------------------------------
	//limpieza de diferencia de evaluación d entradas..
	this->INPUT_EVALUATION_DIFFERENCE	= -1;
}
