//#1: método para diferenciar estructura.
dou ACIS_SD::Differentiate(dou _ac_rg)
{
	//---------------------------------------------------------------------------------
	//---------------------------------------------------------------------------------
	//nivel de congruencia de las estructuras.
	dou congruence = 0;

	//---------------------------------------------------------------------------------
	//---------------------------------------------------------------------------------
	//si _ac_rg es mayor, entonces.
	if (abs(_ac_rg) > abs(this->AC_RG))
	{
		//-----------------------------------------------------------------------------
		//diferenciación de estructuras para obtener congruencia porcentual acumulada.
		congruence += 100.0 - ((abs(_ac_rg - this->AC_RG) / abs(_ac_rg)) * 100.0);
	}

	//---------------------------------------------------------------------------------
	//---------------------------------------------------------------------------------
	//si _ac_rg es menor o igual, entonces.
	else
	{
		//-----------------------------------------------------------------------------
		//diferenciación de estructuras para obtener congruencia porcentual acumulada.
		congruence += 100.0 - ((abs(_ac_rg - this->AC_RG) / abs(this->AC_RG)) * 100.0);
	}

	//---------------------------------------------------------------------------------
	//retorno del nivel de congruencia.
	return congruence;
}

